package vehiclepkg;

public class Vehicle {

	private int noWheels=0;
	
	public int getNoWheels()
	{
		return noWheels;
	}
	
	public void setNoWheels(int noWheels)
	{
		this.noWheels = noWheels;
	}
	
	public String getDrive()
	{
		return drive;
	}
	
	public void setDrive(String drive)
	{
		this.drive = drive;
	}
	private String drive;

}
